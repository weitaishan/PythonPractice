'''
题目：一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同。


'''




'''
假设输入一个五位数a
万位：a//10000           千位：（a//1000）% 10       
十位：（a//10）% 10     个位：a % 10

'''
number = int(input("请输入一个五位的正整数： "))
if number % 10 == number//10000 and (number//10) % 10 == (number//1000) % 10:
        print("是回文数")
else:
        print("不是回文数")

